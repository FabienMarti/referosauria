        <h2 class="m-2 text-center">Qu’est-ce qu’un dinosaure ?</h2>
        <p>“Dinosaure” est un terme qui signifie lézard terriblement grand, bien que certains dinosaures ne dépassent pas la taille d’une poule </p>
        <p>Les dinosaures sont des animaux apparus à l’époque du Trias, il y a environ 230 millions d’années (on cherche encore la date exacte).</p>
        <p>Ils sont le résultat d’une longue évolution des espèces vivantes. De très anciens animaux aquatiques ont abouti au groupe des archosaures, s’étant lui-même divisé en plusieurs groupes, tels que les crocodiles, les ptérosaures (les reptiles volants – le pterodactyle n’était pas un dinosaure) et les dinosaures.</p>
        <p>Pour distinguer ces derniers des autres espèces, les scientifiques ont établi une liste de caractéristiques physiques communes aux dinosaures, parmi lesquelles :</p>
        <ul>
            <li>ils ont une cavité dans le crâne située entre l’oeil et la narine</li>
            <li>leurs chevilles ont une articulation simple comme une charnière</li>
            <li>ils ont une cavité au milieu de la hanche</li>
            <li>ils ont les pattes arrière dressées sous le corps (contrairement aux lézards ou crocodiles, qui ont les pattes de part et d’autre du corps)</li>    
        </ul>
