        <h2 class="m-2 text-center">Les ptérodactyloidés</h2>
        <p>Sur ces derniers la queue s'est raccourcie de façon significative, et la surface arrière de leur membrane de vol s'est réduite considérablement. Ces modifications morphologiques permettent aux ptérodactyles de se mouvoir au sol plus facilement.

Par ailleurs, leurs tailles augmentent, aussi, afin de supporter ces ailes immenses, leurs avant-bras possèdent un os supplémentaire, nommé l’os ptéroïde. Cet os améliore la manoeuvrabilité, au même titre que l’allule des oiseaux.</p>

<p>Pour finir, un fin pelage les recouvre leur peau, ce qui renforce l’idée qu’ils étaient des animaux à sang chaud, ce pelage servant à conserver leur température interne.

Les ptérosaures ne cesseront d’évoluer jusqu’à leur extinction, qui surviendra lors de l’extinction K-T quelques 100 Ma plus tard. Et durant tout ce temps, ils vont conquérir l’ensemble de la planète.
En effet, on découvre des fossiles de ptérosaures sur pratiquement tous les continents. Et  partout où ils s’installent, ils montrent de remarquables adaptations à leur environnement.</p>
