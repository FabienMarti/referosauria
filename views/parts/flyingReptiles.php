        <h2 class="m-2 text-center">Les rhamphorhynchoidés</h2>
        <p>Ce sont les membres de la famille des rhamphorhynchoidés qui sont les premiers à apparaître.

Nous sommes au Carnien, premier étage de la troisième et dernière époque du trias, le trias supérieur, voilà à peu près 230Ma.

Les rhamphorhynchoidés sont en quelque sorte des ptérosaures à l'anatomie primitive.

Car si par la suite au jurassique, les ptérosaures atteindront des tailles phénoménales, pour des animaux ayant la faculté de voler.

Dans un premier temps, ce sont plutôt des créatures de petit gabarit, comme par exemple le Rhamphorhynchus, celui qui a donné son nom à cette famille. Celui-ci mesurait dans les deux mètres d’envergure.</p>
