        <h2 class="m-2 text-center">La pangée</h2>
        <div class="text-center">
            <img src="../assets/img/1200px-Pangaea_200Ma.jpg" alt="" class="img-fluid">
        </div>
        <p>La Pangée (prononcé en français : /pɑ̃.ʒe/) est un supercontinent formé au Carbonifère de la collision de la Laurussia et du Protogondwana et ayant regroupé presque l'ensemble des terres émergées. Au Trias, il s'est morcelé en Laurasia au nord et Gondwana au sud.</p>
