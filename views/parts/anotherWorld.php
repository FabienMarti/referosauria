        <h2 class="m-2 text-center">Histoire</h2>
        <div class="text-center">
            <img src="../assets/img/earthAnimation.gif" alt="">
        </div>
        <p>Le plus ancien supercontinent connu, la Rodinia, se serait fragmenté il y a environ 750 millions d’années. Ces fragments se rassemblèrent au paléozoïque pour former la Pangée, qui se divisa ensuite en deux autres supercontinents, la Laurasia au Nord et le Gondwana au Sud.

            Il semblerait que les supercontinents se forment par cycles, se rassemblant et se fragmentant par le jeu de la tectonique des plaques tous les 400 à 500 millions d’années : c'est le cycle de Wilson. Récemment, les professeurs Rogers et Santosh ont émis l’hypothèse de l’existence de la Columbia, un supercontinent encore antérieur qui se serait formé et divisé entre 1,8 et 1,5 milliard d’années.

            Les géologues ont identifié dans les supercontinents un nombre important de masses continentales qui sont les nôtres aujourd'hui, mais ce ne sont là que des repères car le découpage actuel de nos masses continentales n'existe que depuis la dislocation de la Pangée et l'ouverture de l'Atlantique il y a 200 Ma1.

            Actuellement, on peut définir comme supercontinents l’Eurasie, l’Afrique–Eurasie (encore appelée Afro-Eurasie) et les Amériques.</p>
